# 변수 - 데이터를 저장하기 위한 이름이 붙은 공간
# 상수 - 변하지 않는 값 
x = 10  # x라는 변수에 10이 저장(기억)됨
name = "김기용"
pi = 3.14

# 여러개의 변수 출력시에 ','를 사용 
print(x, name, pi)
print("x =", x)
print("name = ", name)
print("pi =", pi)

x = 100 # 데이터 변경(재할당)
print("x =", x)

# 논리값(True/False)
# 파이썬은 snake법('_'를 사용)
is_merried = True
print("결혼유무:", is_merried)

is_run = False
print("실행유무:", is_run)

